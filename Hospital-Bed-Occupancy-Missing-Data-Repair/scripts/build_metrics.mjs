import fs from "node:fs/promises";
import path from "node:path";
import { SpreadsheetFile, Workbook } from "@oai/artifact-tool";

const root = path.resolve(process.cwd());
const reports = path.join(root, "reports");
const parseCsv = async (name) => {
  const text = await fs.readFile(path.join(reports, name), "utf8");
  const [header, ...lines] = text.trim().split(/\r?\n/);
  const keys = header.split(",");
  return lines.map((line) => Object.fromEntries(line.split(",").map((value, index) => [keys[index], value])));
};
const matrix = (rows, columns) => [columns, ...rows.map((row) => columns.map((key) => {
  const value = row[key] ?? "";
  return value !== "" && !Number.isNaN(Number(value)) ? Number(value) : value;
}))];
const writeTable = (sheet, rows, columns, start = "A1", name = "DataTable") => {
  const values = matrix(rows, columns);
  const columnCount = columns.length;
  const lastColumn = String.fromCharCode(64 + columnCount);
  const lastRow = values.length;
  sheet.getRange(`${start}:${lastColumn}${lastRow}`).values = values;
  const header = sheet.getRange(`${start}:${lastColumn}1`);
  header.format = { fill: "#1F4E78", font: { bold: true, color: "#FFFFFF" }, horizontalAlignment: "center", wrapText: true };
  sheet.getRange(`A2:${lastColumn}${lastRow}`).format.borders = { preset: "all", style: "thin", color: "#D9E2F3" };
  sheet.freezePanes.freezeRows(1);
  return { lastColumn, lastRow };
};

const main = async () => {
  const summary = JSON.parse(await fs.readFile(path.join(reports, "project_summary.json"), "utf8"));
  const modelRows = await parseCsv("model_comparison.csv");
  const imputationRows = await parseCsv("imputation_method_summary.csv");
  const recommendationRows = await parseCsv("imputation_recommendations.csv");
  const statisticalRows = await parseCsv("statistical_results.csv");
  const workbook = Workbook.create();
  const dashboard = workbook.worksheets.add("Dashboard");
  dashboard.showGridLines = false;
  dashboard.getRange("A1:H1").merge();
  dashboard.getRange("A1").values = [["Hospital Bed Occupancy - Metrics Dashboard"]];
  dashboard.getRange("A1:H1").format = { fill: "#0B2545", font: { bold: true, color: "#FFFFFF", size: 16 }, horizontalAlignment: "center" };
  dashboard.getRange("A3:D3").values = [["Records repaired", "Best imputation", "Selected model", "Holdout macro F1"]];
  dashboard.getRange("A4:D4").values = [[summary.records, summary.best_imputation_method, summary.best_model, summary.f1_macro]];
  dashboard.getRange("A3:D3").format = { fill: "#D9EAF7", font: { bold: true, color: "#0B2545" }, horizontalAlignment: "center", wrapText: true };
  dashboard.getRange("A4:D4").format = { fill: "#F8FBFE", font: { bold: true, color: "#17202A" }, horizontalAlignment: "center" };
  dashboard.getRange("D4").format.numberFormat = "0.000";
  dashboard.getRange("A6:D6").merge();
  dashboard.getRange("A6").values = [["Cross-Validated Model Comparison"]];
  dashboard.getRange("A6:D6").format = { fill: "#1F4E78", font: { bold: true, color: "#FFFFFF" } };
  dashboard.getRange(`A7:D${modelRows.length + 7}`).values = matrix(modelRows, ["model", "cv_accuracy_mean", "cv_f1_macro_mean", "cv_accuracy_std"]);
  dashboard.getRange("A7:D7").format = { fill: "#D9EAF7", font: { bold: true, color: "#0B2545" } };
  dashboard.getRange(`B8:D${modelRows.length + 7}`).format.numberFormat = "0.000";
  const chart = dashboard.charts.add("bar", dashboard.getRange(`A7:B${modelRows.length + 7}`));
  chart.title = "CV Accuracy by Model";
  chart.hasLegend = false;
  chart.yAxis = { numberFormatCode: "0.000" };
  chart.setPosition("F3", "N18");
  dashboard.getRange("A1:N30").format.wrapText = true;
  dashboard.getRange("A1:N30").format.columnWidth = 16;
  dashboard.getRange("A1:A30").format.columnWidth = 27;

  const models = workbook.worksheets.add("Model Comparison");
  const modelsInfo = writeTable(models, modelRows, ["model", "cv_accuracy_mean", "cv_accuracy_std", "cv_f1_macro_mean"], "A1", "ModelComparison");
  models.getRange(`B2:D${modelsInfo.lastRow}`).format.numberFormat = "0.000";
  models.getRange("A1:D30").format.columnWidth = 22;
  const imputations = workbook.worksheets.add("Imputation Summary");
  const impInfo = writeTable(imputations, imputationRows, ["method", "rmse", "mae", "distribution_drift", "mean_execution_time_seconds", "downstream_model_accuracy"], "A1", "ImputationSummary");
  imputations.getRange(`B2:F${impInfo.lastRow}`).format.numberFormat = "0.0000";
  imputations.getRange("A1:A30").format.columnWidth = 31;
  imputations.getRange("B1:F30").format.columnWidth = 21;
  const policy = workbook.worksheets.add("Feature Repair Policy");
  const policyInfo = writeTable(policy, recommendationRows, ["feature", "method", "composite_score", "rmse", "mae", "distribution_drift", "downstream_model_accuracy"], "A1", "RepairPolicy");
  policy.getRange(`C2:G${policyInfo.lastRow}`).format.numberFormat = "0.0000";
  policy.getRange("A1:B30").format.columnWidth = 30;
  policy.getRange("C1:G30").format.columnWidth = 20;
  const statistics = workbook.worksheets.add("Statistical Tests");
  const statInfo = writeTable(statistics, statisticalRows, ["test", "comparison", "statistic", "p_value", "effect_size"], "A1", "StatisticalTests");
  statistics.getRange(`C2:E${statInfo.lastRow}`).format.numberFormat = "0.0000";
  statistics.getRange("A1:A30").format.columnWidth = 24;
  statistics.getRange("B1:B30").format.columnWidth = 34;
  statistics.getRange("C1:E30").format.columnWidth = 18;
  const check = await workbook.inspect({ kind: "table", range: "Dashboard!A1:N18", include: "values,formulas", tableMaxRows: 18, tableMaxCols: 14 });
  const errors = await workbook.inspect({ kind: "match", searchTerm: "#REF!|#DIV/0!|#VALUE!|#NAME\\?|#N/A", options: { useRegex: true, maxResults: 50 }, summary: "formula error scan" });
  if (errors.ndjson && /"matches"\s*:\s*\[[^\]]/.test(errors.ndjson)) throw new Error(`Formula validation failed: ${errors.ndjson}`);
  const preview = await workbook.render({ sheetName: "Dashboard", range: "A1:N18", scale: 1, format: "png" });
  await fs.writeFile(path.join(reports, "metrics_dashboard_preview.png"), new Uint8Array(await preview.arrayBuffer()));
  const output = await SpreadsheetFile.exportXlsx(workbook);
  await output.save(path.join(reports, "metrics.xlsx"));
  console.log(check.ndjson);
  console.log("metrics.xlsx created");
};
main().catch((error) => { console.error(error); process.exitCode = 1; });
